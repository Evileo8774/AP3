    function verifyTechnicien($matricule){
        $result = array();
        try{
            $pdo = connectionPDO();
            $req = $pdo -> prepare("SELECT * FROM technicien WHERE matricule = ?");
            $req->execute([$matricule]);
    
            $line = $req->fetch(PDO::FETCH_ASSOC);
            while ($line) {
                $result[] = $line;
                $line = $req->fetch(PDO::FETCH_ASSOC);
            }
    
        } catch(PDOException $e){
            print("Error : ".$e->getMessage());
            die();
        }
        return $result;
    }

